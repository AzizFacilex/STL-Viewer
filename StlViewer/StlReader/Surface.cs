using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;


namespace StlReader
{
   public class Surface
    {
       public line line1, line2, line3;
        public Surface()
        { line1 = new line();
            line2 = new line();
            line3 = new line();
        }
        public override string ToString()
        {
            return (" Surface(" + line1+ ";" + line2 + ";" + line3 + ") ");
        }
    }
   
}
